import Foundation
import UIKit

extension UIColor {
    
    class func gray184() -> UIColor {
        return UIColor(
            red: 184.0 / 255.0,
            green: 184.0 / 255.0,
            blue: 184.0 / 255.0,
            alpha: 1.0
        )
    }
    
    class func lightGray252() -> UIColor {
        return UIColor(
            red: 252.0 / 255.0,
            green: 252.0 / 255.0,
            blue: 252.0 / 255.0,
            alpha: 1.0
        )
    }
    
    class func almostWhite254() -> UIColor {
        return UIColor(
            red: 254.0 / 255.0,
            green: 252.0 / 255.0,
            blue: 254.0 / 255.0,
            alpha: 1.0
        )
    }
    
    
}
